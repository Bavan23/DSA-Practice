num1=int(input("Enter the value of num1-"))
num2=int(input("Enter the value of num2-"))
operation=input("add/sub/mul/div-")

if(operation=="add"):
    print("The output is",num1+num2)
elif(operation=="mul"):
    print("The output is",num1*num2)
elif(operation=="sub"):
    print("The output is",num1-num2)
elif(operation=="div"):
    print("The output is",num1/num2)
else:
    print("Enter a Valid Operator")
